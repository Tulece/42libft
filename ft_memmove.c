#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*temp;

	i = 0;
	temp = (char *)s;
	while (n > 0)
	{
		temp[i] = '\0';
		i++;
		n--;
	}
}
